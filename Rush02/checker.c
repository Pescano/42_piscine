#include "ft_lib.h"

int	ft_checker(char *str)
{
	int	x;

	if (*str == '\0')
		return (0);
	else
	{
		while (*str != '\0')
		{
			if (*str >= '0' && *str <= '9')
				x = 1;
			else
				return (0);
			str++;
		}
	}
	return (x);
}
