#include <stdio.h>

int	ft_recursive_power(int nb, int power)
{
	int	x;

	x = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (power > 1)
		x = nb * ft_recursive_power(nb, (power - 1));
	return (x);
}
