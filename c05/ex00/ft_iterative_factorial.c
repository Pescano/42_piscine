#include <stdio.h>

int	ft_iterative_factorial(int nb)
{
	int	x[2];

	x[0] = 1;
	x[1] = 1;
	if (nb < 0)
		return (0);
	while (x[0] <= nb)
	{
		x[1] = x[1] * x[0];
		x[0]++;
	}
	return (x[1]);
}
